    def get_route(self, start_loc: str, end_loc: str, aircraft_type: str) -> Optional[List[str]]:
        """
        根据起降地点和机型查询航路
        
        参数：
            start_loc: 起飞地点名称
            end_loc: 降落地点名称
            aircraft_type: 机型名称（用于判断使用民航还是军用航路）
        
        返回：
            航路点名称列表，未找到返回起降两点的简单航路
        """
        start_geo = self.get_geo_location(start_loc)
        end_geo = self.get_geo_location(end_loc)
        
        if not start_geo or not end_geo:
            return None
        
        # 根据机型判断航路类型
        aircraft_perf = self.get_aircraft_performance(aircraft_type)
        # 巡航速度<300m/s的认为是民航，否则是军用
        route_type = '民航' if aircraft_perf and '民航' in aircraft_perf.name or aircraft_perf.cruise_speed_ms < 300 else '军用'
        
        # 在航路库中查找匹配的航路
        for route_name, route_info in self.routes.items():
            if route_info['type'] != route_type:
                continue
            waypoints = route_info['waypoints']
            if len(waypoints) < 2:
                continue
            
            # 获取航路中所有点的名称
            start_names = [self.get_geo_location(wp).name if self.get_geo_location(wp) else wp for wp in waypoints]
            
            # 检查起降点是否都在航路中
            if start_geo.name in start_names and end_geo.name in start_names:
                start_idx = start_names.index(start_geo.name)
                end_idx = start_names.index(end_geo.name)
                # 返回正确方向的航路点序列
                if start_idx < end_idx:
                    return waypoints[start_idx:end_idx + 1]
                else:
                    return list(reversed(waypoints[end_idx:start_idx + 1]))
        
        # 未找到匹配航路，返回简单两点航路
        return [start_geo.name, end_geo.name]

    def get_waypoint(self, name: str) -> Optional[Waypoint]:
        """
        根据名称查询导航点
        
        参数：
            name: 导航点名称或机场名称
        
        返回：
            Waypoint对象，未找到返回None
        """
        # 在导航点库中查找
        if name in self.waypoints:
            return self.waypoints[name]
        
        # 在机场库中查找
        geo = self.get_geo_location(name)
        if geo:
            return Waypoint(geo.name, geo.lon, geo.lat, geo.alt_m)
        
        return None

    def list_airports(self) -> List[str]:
        """返回所有机场名称列表"""
        return list(self.geo_locations.keys())

    def list_aircraft(self) -> List[str]:
        """返回所有机型名称列表"""
        return list(self.aircraft_performance.keys())

    def list_routes(self) -> List[str]:
        """返回所有航路名称列表"""
        return list(self.routes.keys())

    def list_missions(self) -> List[str]:
        """返回所有任务类型名称列表"""
        return [m.value for m in MissionType]
