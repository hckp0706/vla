    def get_geo_location(self, name: str) -> Optional[GeoLocation]:
        """
        根据地名查询地理位置
        
        支持精确匹配和模糊匹配：
            - 精确匹配：名称完全一致
            - 包含匹配：名称包含查询字符串
            - 相似度匹配：字符串相似度>=0.4
        
        参数：
            name: 地名关键字，如"北京大兴国际机场"或"大兴"
        
        返回：
            GeoLocation对象，未找到返回None
        """
        # 精确匹配
        if name in self.geo_locations:
            return self.geo_locations[name]
        
        # 包含匹配
        for key in self.geo_locations:
            if name in key or key in name:
                return self.geo_locations[key]
        
        # 相似度匹配
        best_match = None
        best_ratio = 0.0
        for key in self.geo_locations:
            ratio = SequenceMatcher(None, name, key).ratio()
            if ratio > best_ratio:
                best_ratio = ratio
                best_match = key
        
        if best_ratio >= 0.4:
            return self.geo_locations[best_match]
        return None

    def get_aircraft_performance(self, name: str) -> Optional[AircraftPerformance]:
        """
        根据机型名称查询机型性能
        
        支持精确匹配和模糊匹配
        
        参数：
            name: 机型名称，如"民航客机"、"歼-20"
        
        返回：
            AircraftPerformance对象，未找到返回None
        """
        # 精确匹配
        if name in self.aircraft_performance:
            return self.aircraft_performance[name]
        
        # 相似度匹配
        best_match = None
        best_ratio = 0.0
        for key in self.aircraft_performance:
            ratio = SequenceMatcher(None, name, key).ratio()
            if ratio > best_ratio:
                best_ratio = ratio
                best_match = key
        
        if best_ratio >= 0.5:
            return self.aircraft_performance[best_match]
        return None

